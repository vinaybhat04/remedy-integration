package org.opennms.plugin.ticketer.jira.integration.remedy.bean;

public class RemedyTemplateDTO {
	
	private String templateName;
	
	private String summary;
	
	private String detailedDescription;
	
	private String impact;
	
	private String urgency;

	public String getTemplateName() {
		return templateName;
	}

	public void setTemplateName(String templateName) {
		this.templateName = templateName;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public String getDetailedDescription() {
		return detailedDescription;
	}

	public void setDetailedDescription(String detailedDescription) {
		this.detailedDescription = detailedDescription;
	}

	public String getImpact() {
		return impact;
	}

	public void setImpact(String impact) {
		this.impact = impact;
	}

	public String getUrgency() {
		return urgency;
	}

	public void setUrgency(String urgency) {
		this.urgency = urgency;
	}

	@Override
	public String toString() {
		return "RemedyTemplateDTO [templateName=" + templateName + ", summary=" + summary + ", detailedDescription="
				+ detailedDescription + ", impact=" + impact + ", urgency=" + urgency + "]";
	}
	
	

}
